function createParticle(x, y) {
  let c = aqiColorArr(getAQIatX(x));
  return {
    x,
    y,
    vx: random(-1.5, 1.5),
    vy: random(-2.5, -0.5),
    life: 1.0,
    col: c,
    size: random(3, 7),
  };
}
function updateParticles() {
  for (let p of particles) {
    p.x += p.vx;
    p.y += p.vy;
    p.life -= 0.032;
    p.vy *= 0.96;
  }
  particles = particles.filter((p) => p.life > 0);
}
