// ── Helpers ───────────────────────────────────────────────────
function getBand(aqi) {
  for (let i = 0; i < BAND_MAX.length; i++) if (aqi <= BAND_MAX[i]) return i;
  return BAND_MAX.length - 1;
}

function aqiColorArr(aqi) {
  let band = getBand(aqi);
  let lo = band === 0 ? 0 : BAND_MAX[band - 1];
  let hi = BAND_MAX[band];
  let t = constrain(map(aqi, lo, hi, 0, 1), 0, 1) * 0.5;
  let cLo = PALETTE[band];
  let cHi = band < PALETTE.length - 1 ? PALETTE[band + 1] : PALETTE[band];
  return [
    lerp(cLo[0], cHi[0], t),
    lerp(cLo[1], cHi[1], t),
    lerp(cLo[2], cHi[2], t),
  ];
}

// conditionColor defined above replaces healthColorArr

// Age factor: children (<12) and elderly (>60) are more vulnerable
function ageFactor(age) {
  if (age <= 12) return map(age, 5, 12, 2.0, 1.4);
  if (age <= 40) return map(age, 12, 40, 1.4, 1.0);
  if (age <= 60) return map(age, 40, 60, 1.0, 1.4);
  return map(age, 60, 80, 1.4, 2.2);
}

// Daily damage per unit AQI (scaled so AQI 500 causes ~1.5 pts/day at age 25)
// years=0 means snapshot (30-day baseline); years>0 extends damage accumulation
function computeHealth(aqi, age, years) {
  let af = ageFactor(age + (years || 0) / 2); // age drifts over window
  let dailyDamage = max(0, map(aqi, 0, 500, 0, 3.0) * af - 0.2);
  let days = years > 0 ? 365 * years : 30;
  return constrain(100 - dailyDamage * days, 0, 100);
}

function condition(h) {
  if (h >= 90) return "Healthy — no risk";
  if (h >= 75) return "Mild irritation — occasional cough";
  if (h >= 60) return "Moderate — eye & throat irritation";
  if (h >= 45) return "Elevated — reduced lung capacity";
  if (h >= 30) return "High risk — asthma, bronchitis";
  if (h >= 15) return "Severe — COPD, cardiac stress";
  return "CRITICAL — hazardous exposure";
}

function conditionColor(h) {
  if (h >= 90) return [46, 213, 115];
  if (h >= 75) return [130, 230, 100];
  if (h >= 60) return [255, 220, 50];
  if (h >= 45) return [255, 160, 26];
  if (h >= 30) return [230, 90, 40];
  if (h >= 15) return [210, 50, 60];
  return [255, 60, 80];
}

function drawTrophyButton(x, y, size) {
  if (!showTrophy || showCongrats) return;

  // background circle
  fill(20, 30, 50);
  stroke(255, 200);
  strokeWeight(2);
  ellipse(x, y, size + 10);

  // circular masked image
  push();
  drawingContext.save();

  drawingContext.beginPath();
  drawingContext.arc(x, y, size / 2, 0, TWO_PI);
  drawingContext.closePath();
  drawingContext.clip();

  imageMode(CENTER);
  image(trophyImg, x, y, size, size);
  imageMode(CORNER);

  drawingContext.restore();
  pop();

  // store clickable circle
  trophyBounds = { x, y, r: size / 2 };
}

function handleTrophyClick() {
  if (showTrophy && !showCongrats && trophyBounds) {
    let d = dist(mouseX, mouseY, trophyBounds.x, trophyBounds.y);
    if (d < trophyBounds.r) {
      showCongrats = true;
      return true;
    }
  }
  return false;
}

function drawCongratsScreen() {
  if (!showCongrats) return;

  background(0);
  imageMode(CENTER);
  image(congratsImg, width / 2, height / 2, width, height);
  imageMode(CORNER);
}

function updateTrophyTrigger(bx, endX) {
  if (bx >= endX) {
    showTrophy = true;
  } else {
    showTrophy = false;
    showCongrats = false; // reset when user moves back
  }
}
